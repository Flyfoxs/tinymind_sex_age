    logger.debug(f"These columns{exclude} will be drop from feature:{exclude_columns}")

    mini_df = df[[col for col in df.columns if col not in exclude_columns]]
    for col in require_list:
        mini_df[col] = df[col]
    logger.debug(f"Convert the column from {len(df.columns)} to {len(mini_df.columns)}")
    return mini_df


